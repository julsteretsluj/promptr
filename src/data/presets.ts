import type { Routine } from '../types'

const step = (label: string, detail?: string): { label: string; detail?: string } => ({
  label,
  detail,
})

function withIds(routine: Omit<Routine, 'steps'> & { steps: { label: string; detail?: string }[] }): Routine {
  return {
    ...routine,
    steps: routine.steps.map((s, i) => ({
      id: `${routine.id}-${i}`,
      label: s.label,
      detail: s.detail,
    })),
  }
}

